; Keyboard echo program. Polls the keyboard and draws typed characters.
; Assemble with SCREEN_WIDTH=<display width> and BYTES_PER_ROW=(SCREEN_WIDTH / 8).

.include "stdlib/io.asm"

.equ CURSOR_ADDRESS, 0x02
.equ CURSOR_COLUMN, 0x03
.equ LINE_BASE_ADDRESS, 0x04

JMP Start
.byte 0, 0, 0

Start:
LDI R0, IO_DISPLAY, R3
OUT ADDR, R0
LDI R0, IO_KEYBOARD, R3
OUT ADDR, R0

Poll:
LDI R0, IO_KEYBOARD, R3
OUT ADDR, R0
IN DATA, R1

LDI R0, 0, R3
CLF
CMP R1, R0
JE Poll

LDI R0, 13, R3
CLF
CMP R1, R0
JE Enter

LDI R0, 8, R3
CLF
CMP R1, R0
JE Backspace

JMP16 Printable, R0, R3

Enter:
LDI R0, LINE_BASE_ADDRESS, R3
LD R2, [R0]
LDI R3, SCREEN_WIDTH, R1
ADD R2, R3
LDI R0, LINE_BASE_ADDRESS, R3
ST [R0], R2
LDI R0, CURSOR_ADDRESS, R3
ST [R0], R2
LDI R1, 0, R3
LDI R0, CURSOR_COLUMN, R3
ST [R0], R1
JMP16 Poll, R0, R3

Backspace:
LDI R0, CURSOR_COLUMN, R3
LD R2, [R0]
LDI R0, 0, R3
CLF
CMP R2, R0
JE Poll

LDI R0, CURSOR_COLUMN, R3
LD R2, [R0]
LDI R3, 0xFFFF, R1
ADD R2, R3
LDI R0, CURSOR_COLUMN, R3
ST [R0], R2

LDI R0, CURSOR_ADDRESS, R3
LD R2, [R0]
LDI R3, 0xFFFF, R1
ADD R2, R3
LDI R0, CURSOR_ADDRESS, R3
ST [R0], R2

LDI R1, ' ', R3
JMP Printable

Printable:
LDI R0, 0, R3
ADD R0, R1
SHL R0
SHL R0
SHL R0
LDI R3, FONT_BASE, R2
ADD R0, R3
LDI R2, CURSOR_ADDRESS, R3
LD R2, [R2]

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1
LDI R3, 1, R1
ADD R0, R3
LDI R3, BYTES_PER_ROW, R1
ADD R2, R3

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1
LDI R3, 1, R1
ADD R0, R3
LDI R3, BYTES_PER_ROW, R1
ADD R2, R3

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1
LDI R3, 1, R1
ADD R0, R3
LDI R3, BYTES_PER_ROW, R1
ADD R2, R3

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1
LDI R3, 1, R1
ADD R0, R3
LDI R3, BYTES_PER_ROW, R1
ADD R2, R3

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1
LDI R3, 1, R1
ADD R0, R3
LDI R3, BYTES_PER_ROW, R1
ADD R2, R3

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1
LDI R3, 1, R1
ADD R0, R3
LDI R3, BYTES_PER_ROW, R1
ADD R2, R3

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1
LDI R3, 1, R1
ADD R0, R3
LDI R3, BYTES_PER_ROW, R1
ADD R2, R3

LD R1, [R0]
OUT ADDR, R2
OUT DATA, R1

LDI R0, CURSOR_ADDRESS, R3
LD R2, [R0]
LDI R3, 1, R1
ADD R2, R3
LDI R0, CURSOR_ADDRESS, R3
ST [R0], R2

LDI R0, CURSOR_COLUMN, R3
LD R2, [R0]
LDI R3, 1, R1
ADD R2, R3
LDI R0, CURSOR_COLUMN, R3
ST [R0], R2

JMP16 Poll, R0, R3

.org FONT_BASE
.incbin "assets/font8x8.bin"
